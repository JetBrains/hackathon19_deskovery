  MX_USART3_UART_Init();
  /* USER CODE BEGIN 2 */
    deskoveryInit();

    VLO53L1A1_ResetPin(0); // Shutdown center sensor
    HAL_Delay(2);
    VLO53L1A1_ResetPin(1); // run center sensor
    HAL_Delay(2);
    HAL_ADCEx_InjectedStart_IT(&hadc1);

    setupSensor(&centerSensor);
    LCD5110_init();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
#pragma clang diagnostic push
